def category_for(f: dict[str, str]) -> str:
    slug = f["slug"]
    prefixes = (
        ("tc-", "Transaction capture"),
        ("pm-", "Pending management"),
        ("cm-", "Confirmed transactions"),
        ("rq-", "Reports & queries"),
        ("db-", "Dashboard & charts"),
        ("st-", "Settings & profile"),
        ("ds-", "Data storage & export"),
        ("ux-", "Chat UX"),
        ("l-", "Localization"),
        ("v-", "Phase 0.5 (Vietnamese)"),
        ("bg-", "Budgets & goals"),
        ("rc-", "Recurring"),
        ("au-", "Auth & deployment"),
        ("mb-", "Mobile & PWA"),
        ("in-", "Integrations"),
        ("eq-", "Quality & testing"),
        ("ac-", "Payment accounts"),
        ("eng-", "Engineering platform"),
        ("oos-", "Out of scope"),
    )
    for prefix, name in prefixes:
        if slug.startswith(prefix):
            return name
    return "Other"


def write_readme(features: list[dict[str, str]]) -> None:
    lines = [
        "# FinGuard feature specs",
        "",
        "One RFC-style spec per feature. Generated by `build_all_specs.py`.",
        "",
        "**Catalog:** [USE_CASE_CATALOG](../USE_CASE_CATALOG.md) · "
        "**Plan:** [PRODUCT_PLAN](../PRODUCT_PLAN.md) · "
        "**Roadmap:** [ROADMAP](../ROADMAP.md)",
        "",
        f"**Total specs:** {len(features)}",
        "",
        "## How to review (PO)",
        "",
        "1. Open a spec from the tables below (or search by ID, e.g. `V-1`, `TC-09`).",
        "2. Read **Problem**, **Proposal**, **Scope**, and **Acceptance criteria**.",
        "3. Check **Build**, **Defer**, or **Reject** in the **PO decision** section; add date and notes.",
        "4. For **Defer**, note target phase; for **Reject**, note why (duplicate, out of vision, etc.).",
        "5. Update [PRODUCT_PLAN](../PRODUCT_PLAN.md) phase backlog when you commit to a batch of **Build** decisions.",
        "",
        "**Suggested review order:** Phase **0.5** (V-* / TC-09–11 / PM-10 / RQ-07) → Phase **1** (CM-04+, DB-06+) → Phase **1.5** (AC-*) → Phase **2** (BG-*) → Future / Roadmap / Out of scope last.",
        "",
        "Regenerate after editing feature definitions in `build_all_specs.py`: "
        "`python docs/specs/build_all_specs.py` (re-run overwrites generated `.md` files).",
        "",
        "---",
        "",
    ]
    current_cat = None
    for f in features:
        cat = category_for(f)
        if cat != current_cat:
            if current_cat is not None:
                lines.append("")
            lines.extend([
                f"## {cat}",
                "",
                "| Spec ID | Title | Status | Phase | Priority | Effort | Spec file | Build decision |",
                "|---------|-------|--------|-------|----------|--------|-----------|----------------|",
            ])
            current_cat = cat
        lines.append(
            f"| {f['id']} | {f['title']} | {f['status']} | {f['phase']} | "
            f"{f['priority']} | {f['effort']} | [{f['slug']}.md](./{f['slug']}.md) | |"
        )
    lines.append("")
    (SPECS_DIR / "README.md").write_text("\n".join(lines) + "\n")


def main() -> None:
    for f in FEATURES:
        path = SPECS_DIR / f"{f['slug']}.md"
        path.write_text(render_spec(f))
    write_readme(FEATURES)
    print(f"Wrote {len(FEATURES)} spec files + README.md")


if __name__ == "__main__":
    main()
