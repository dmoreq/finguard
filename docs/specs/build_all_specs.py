def current_state(status: str) -> str:
    return STATUS_CURRENT.get(status, "Status documented in USE_CASE_CATALOG.")


def related_links(related: str) -> str:
    if not related or related.strip() in ("—", "-", "N/A"):
        return "—"
    parts = [s.strip() for s in related.split(",")]
    return ", ".join(
        f"[{p.replace('.md', '')}](./{p})" for p in parts if p
    )


